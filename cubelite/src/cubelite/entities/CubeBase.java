package cubelite.entities;

public class CubeBase {
	public int x, y, width, height, xvel, yvel;
	
	public CubeBase(int x, int y, int width, int height, int xvel, int yvel){
		this.x = x;
		this.y = y;
		this.width = width;
		this.height = height;
		this.xvel = xvel;
		this.yvel = yvel;
	}
}
